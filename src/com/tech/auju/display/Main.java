    private void btnBeliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBeliActionPerformed
        // TODO add your handling code here:
        if(lstBeli1.isSelectionEmpty() == false) {
            pi.setListBeli(lstBeli1.getSelectedIndex());
            lstBeli2.setModel(pi.getBeliListModel());
            lstBeli1.setModel(pi.getListModel());
            lblTotPengeluaran.setText("Total pengeluaran : Rp " + pi.getTotalHarga());
            btnPesan.setEnabled(true);
        }        
    }//GEN-LAST:event_btnBeliActionPerformed

    private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelActionPerformed
        // TODO add your handling code here:
        if(lstBeli2.isSelectionEmpty() == false) {
            pi.removeListBeli(lstBeli2.getSelectedIndex());
            lstBeli2.setModel(pi.getBeliListModel());
            lstBeli1.setModel(pi.getListModel());
            lblTotPengeluaran.setText("Total pengeluaran : Rp " + pi.getTotalHarga());
            if(lstBeli2.getModel().getSize() < 1)
                btnPesan.setEnabled(false);
        }
    }//GEN-LAST:event_btnDelActionPerformed

