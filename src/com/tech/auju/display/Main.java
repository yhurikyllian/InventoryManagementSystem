    /**
     * Fill table with current data
     * from database.
     */
    private void initTable() {
        pi = new PembelianImpl();
        tblCurrent.setModel(pi.getTableModel(CURRENT_STOCK));
        pi.fillTable(tblCurrent, CURRENT_STOCK);
        tblRetur.setModel(pi.getTableModel(RETUR));
        pi.fillTable(tblRetur, RETUR);
    }
    
    /**
     * Change menu interface.
     * 
     * @param option index of menu
     */
    private void changeMenu(int option) {
        if(option == CURRENT_STOCK) {
            pnlCurrStocks.setVisible(true);
            pnlEmptyStocks.setVisible(false);
            pnlRetur.setVisible(false);
        } else if(option == EMPTY_STOCK) {
            pnlCurrStocks.setVisible(false);
            pnlEmptyStocks.setVisible(true);
            pnlRetur.setVisible(false);
        } else if(option == RETUR) {
            pnlCurrStocks.setVisible(false);
            pnlEmptyStocks.setVisible(false);
            pnlRetur.setVisible(true);
        }
    }
    
    
