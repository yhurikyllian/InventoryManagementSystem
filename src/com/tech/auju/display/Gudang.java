    private void tableRecordListener() {
        this.pembelian = gudangImpl.getPembelian(tblBaru.getSelectedRow());
        String text = "Nama bahan : " + pembelian.getBahan().getNama_bahan() + "\nJumlah : " +
                pembelian.getBahan().getJumlah_bahan() + "\nHarga satuan : " +
                pembelian.getBahan().getHarga_satuan() + "\nMerk : " +
                pembelian.getBahan().getMerk().getNama_merk() + "\nWarna : " +
                pembelian.getBahan().getWarna().getNama_warna() + "\nJenis : " +
                pembelian.getBahan().getJenis().getNama_jenis() + "\nSupplier : " +
                pembelian.getBahan().getSupplier().getNama_supplier() + "\nAlamat supplier : " +
                pembelian.getBahan().getSupplier().getAlamat_supplier();
        
        txtDetail.setText(text);
    }
    
