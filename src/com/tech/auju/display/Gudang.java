    private void btnReturActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReturActionPerformed
        // TODO add your handling code here:
        if(JOptionPane.YES_OPTION == JOptionPane.showConfirmDialog(this, "Are you sure to return this product?", "Attention", JOptionPane.YES_NO_OPTION)) {
            gudangImpl.returnPembelian(tblBaru.getSelectedRow()); 
            gudangImpl.remove(tblBaru.getSelectedRow());
            
            tblRetur.setModel(gudangImpl.getTableModel(RETUR));
            gudangImpl.updateReturTable(tblRetur);
            
            tblBaru.setModel(gudangImpl.getTableModel(PEMBELIAN));
            gudangImpl.updatePembelianTable(tblBaru);         
        }        
    }//GEN-LAST:event_btnReturActionPerformed

